    private void Search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = RollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM result WHERE rollNo='"+rollNo+"'");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered Roll No doesn't exist");
            }
            else{  
                RollNo_TextField.setEditable(false);
                Physics_TextField.setText(rs.getString(2));
                Chemistry_TextField.setText(rs.getString(3));
                Maths_TextField.setText(rs.getString(4));
                Electrical_TextField.setText(rs.getString(5));
                Comm_TextField.setText(rs.getString(6));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_Search_ButtonActionPerformed

