    private void Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit_ButtonActionPerformed
        // TODO add your handling code here:
        String name = Name_TextField.getText();
        String rollNo = RollNo_TextField.getText();
        String gender = (String)Gender_ComboBox.getSelectedItem();
        String fatherName = FatherName_TextField.getText();
        String course = (String)Course_ComboBox.getSelectedItem();
        String branch = (String)Branch_ComboBox.getSelectedItem();
        if(name.length()==0 || rollNo.length()==0 || fatherName.length()==0){
            JOptionPane.showMessageDialog(null, "Plzzz fill the details");
        }
        else{
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student VALUES('"+name+"','"+rollNo+"','"+gender+"','"+fatherName+"','"+course+"','"+branch+"')");
            JOptionPane.showMessageDialog(null, "Successfully Inserted");
            setVisible(false);
            InsertStudent frame = new InsertStudent();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        }
    }//GEN-LAST:event_Submit_ButtonActionPerformed

