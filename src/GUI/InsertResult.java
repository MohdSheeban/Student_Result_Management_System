    private void Search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = RollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM student WHERE rollNo='"+rollNo+"'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered Roll No doesn't exist");
            }
            else{  
                RollNo_TextField.setEditable(false);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_Search_ButtonActionPerformed

    private void Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = RollNo_TextField.getText();
        int phyMarks = Integer.parseInt(Physics_TextField.getText());
        int chemMarks = Integer.parseInt(Chemistry_TextField.getText());
        int mathsMarks = Integer.parseInt(Maths_TextField.getText());
        int electricalMarks = Integer.parseInt(Electrical_TextField.getText());
        int commMarks = Integer.parseInt(Comm_TextField.getText());
        int total = phyMarks + chemMarks + mathsMarks + electricalMarks + commMarks;
        
        try{
            
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into result values("+ rollNo + ", " + phyMarks +", "+ chemMarks +", "+ mathsMarks +", "+ electricalMarks +", "+ commMarks + ", " + total + ")");
            JOptionPane.showMessageDialog(null, "Added Succesfully");
            setVisible(false);
            InsertResult frame = new InsertResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_Submit_ButtonActionPerformed

